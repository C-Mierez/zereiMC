public class Mostrar {
    
    
    public static void arregloUni(int[] a){
        
        for(int i = 0; i < a.length; i++){
            System.out.print(a[i] + " ");
        }
        
        System.out.println();
        
    }
    
    public static void arregloUniString(String[] a){
        
        for(int i = 0; i < a.length; i++){
            System.out.print(a[i] + " ");
        }
        
        System.out.println();
        
    }
}
